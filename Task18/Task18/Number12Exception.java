package com.Task18;

public class Number12Exception extends Exception {
    public Number12Exception() {
    }

    public Number12Exception(String message) {
        super(message);
    }

    public Number12Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public Number12Exception(Throwable cause) {
        super(cause);
    }

    public Number12Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
